package in.co.rays.basisc;

public class Mathclass {
     public static void main(String[] args) {
	
     System.out.println("Mathematics operation");
     System.out.println("Max 2,25="       +Math.max(25,5));
     System.out.println("Min 32,5="       +Math.min(32,5));
     System.out.println("Absolute3.7="    +Math.abs(3.7));
     System.out.println("Exp 10="         +Math.exp(10));
     System.out.println("Random Number="  +Math.random()*100);
     System.out.println("Square Root="    +Math.sqrt(16));
     
}}

